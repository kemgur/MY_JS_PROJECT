/*
Primitive values, like "John Doe", 
cannot have properties or methods (because they are not objects).

But with JavaScript, methods and properties are 
also available to primitive values, 
because JavaScript treats primitive values as objects
 when executing methods and properties.

 Property:    	Description:
constructor     Returns the string's constructor function

length      	Returns the length of a string

prototype   	Allows you to add properties and methods to an object

*/