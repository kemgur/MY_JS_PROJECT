/*
The JavaScript global properties and functions can be used with all the built-in JavaScript objects.
JavaScript global özellikleri ve işlevleri, tüm yerleşik JavaScript nesneleriyle kullanılabilir.

It makes sense to call the list above global functions rather than global methods because the functions are called globally and not any objects.
Listeyi global metotlardan ziyade global fonksiyonların üstünde çağırmak mantıklıdır çünkü fonksiyonlar herhangi bir nesne değil global olarak adlandırılır.

Anyway, you can also call these functions methods, since they are methods of the global object where they run. In a web browser, the global object is the browser window. Then isNaN() is actually a window method: window.isNaN().
Her neyse, çalıştıkları global nesnenin yöntemleri olduklarından, bu işlevler yöntemlerini de çağırabilirsiniz. Bir web tarayıcısında, genel nesne tarayıcı penceresidir. O zaman isNaN () aslında bir pencere yöntemidir: window.isNaN ().
*/

